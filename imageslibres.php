                <h1 class="text-center">#web : Les banques d'<span>images</span> libres</h1>
            </div>

            <div class="col-12">
            
            <p class="text-justify">Si vous êtes rédacteur, et que vous possédez un site sur lequel vous publiez de temps en temps des articles, vous vous êtes sans doute déjà retrouvé face au syndrome de « l’image blanche » : Où allez-vous donc trouver une image pour illustrer votre publication ? La tentation d’aller puiser sur le net est grande. Il suffit d’une petite recherche sur votre moteur de recherche préféré, d’un petit clic droit sur l’image et hop ! L’image est sur votre disque dur, mais savez-vous que ce n’est pas bien, pas bien du tout ? Avez-vous déjà entendu parlé du « droit d’auteur » ? Non ? C’est sympa comme truc, c’est l’ensemble des droits dont dispose un auteur ou ses ayant droits sur des œuvres. Pour résumer, si vous prenez une image sur le net sans en avoir la permission, c’est quasi du vol.</p>

            <p class="text-justify">C’est pourquoi je vous propose sur cette page une petite liste des banques d'images que j’utilise dans le cadre de mon boulot de développeur web. Elles proposent pour la plupart du contenu « libre de droit », ce qui signifie, pour faire simple, que l’auteur autorise n’importe qui à utiliser sa création gratuitement et sans rien d'autre en retour.</p>

