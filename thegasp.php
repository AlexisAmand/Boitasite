			<br />

        	<p class="text-justify">Il y a quelques mois, j'ai découvert un petit listing en Basic paru dans le n°38 de Jeux et Stratégie d'avril/mai 1985 (<a href="thegasp.pdf">voir l'article de l'époque</a>). Il s'agit d'un petit jeu de réflexion qui est un petit mélange de Reverso et d'Othello. Vous disposez d'un plateau de jeu avec 16 pions bicolores formant un carré 4 x 4. Le but est de retourner tous les pions en respectant une seule régle : Quand vous désignez un pion, ses 8 voisins changent de couleurs mais lui garde la sienne.</p><br />

    		<p class="text-justify">Quelques pages plus tard, la revue adaptait le jeu de plateau en Basic. Rien de bien génial, aujourd'hui nous appelerions ça un jeu en "mode console".</p><br />

    		<p class="text-justify">Dans la version que je vous propose ici, les pions sont remplacés par des cartes, mais le principe est toujours le même : Quand vous désignez une carte, ses 8 voisines sont retournées sauf elle. Saurez-vous toutes les retourner ?</p><br />
	        
