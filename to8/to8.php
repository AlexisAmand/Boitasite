                <h2 style="margin-top:20px;margin-bottom:10px;">Présentation du TO8</h2>

                <p>(source: TEOphile n°6 de septembre 1986)</p><br />    

                <p>Le TO8 est une machine ouverte et évolutive proposée à 2990 F dans sa version de base mais qui peut devenir l'équivalent d'un TO9+ avec une extension de mémoire et une unité de disquette. Le TO8 est blanc également, son clavier de 81 touches  + pavé numérique + 5 touches de fonctions, identique à celui du TO9 est, comme le TO7-70, intégré dans l'unité centrale. Au dessus du clavier, on trouve la trappe verticale à mémo 7. Doté de 256K de RAM il peut recevoir à l'arrière sur un connecteur spécial une extension de 256K portant la mémoire à 512 K comme le TO9+. Les 80 K de ROM contiennent le Basic 512 (identique au Basic 128 mais gérant les banques de RAM jusqu'à 512), le Basic Basic 1.0 et le moniteur.</p><br />

                <figure style="text-align:center;width:100%;border:0px solid black;">
                <a href="to8/images/thomson_t08_04.jpg" id="single_image">
                <img src="to8/images/thumbs/thomson_t08_04.jpg" width="50%" alt="capture d'écran de la page de démarrage du TO8"/></a>
                <figcaption>On allume le TO8 !</figcaption>
                </figure>
                
                <br />

                <p>Le TO8 possède un contrôleur de disquette 3,5 pouces 640K fonctionnant avec le DOS inclus dans le Basic  512. Un connecteur pour brancher l'unité de disquettes (Sony) apparaît à l'arrière de l'unité centrale. Ce connecteur peut recevoir également un QDD. Plusieurs unités de disquettes sont connectables par chaînage. On trouve aussi le sunthétiseur de sons identique à celui du MO6, une interface pour imprimante paralléle, un connecteur pour LEP (identique à celui du TO7, mais avec un nouveau design), deux connecteurs pour manettes de jeux/souris, une prise son Cinch et un seul connecteur d'extension pour recevoir un modem, etc. Le TO8 adopte, comme toute la gamme, les 8 modes d'affichage du TO9 dont le mode 80 colonnes et la palette de 4096 couleurs. La version de base est proposée à 2990 F ttc, l'unité de disquettes à 3,5 pouces 640 K à 1990 F ttc. Une configuration comprenant un TO8, <a href="to8/moniteur-to8.php">un moniteur couleurs haute résolution</a> et une unité de disquette 640 K sera proposée à 5990 F ttc au lieu de 7470 F.</p>
                
