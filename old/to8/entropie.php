            Entropie sur TO8
            </p>

            <br />

            <p>Comme dans tous les jeux d'aventure et d'action, sa marche est entravée par une multitude de robots et de formes bizarroïdes. Mais le premier obstacle est sans nul doute le contrôle des déplacements. Les directions sont calculées en fonction de la position du robot et non de celle du joueur, ce qui est toujours déroutant au début.<br /><br />
            Lors des premières parties, Explorer 3 limite son parcours à quelques salles immédiatement accessibles avant de mourir prématurément. Puis, peu à peu, il découvre de nouveaux horizons, fait parler les objets et déméle l'écheveau des forces en présences. L'animations est rapide et déliée et les graphismes sont réussis malgré la quasi-absence de couleurs. La première aventure soi-disant logique est déjà très ardue. Imaginez les scénarios aléatoires que l'on appelle lors du chargement... DE beaux sacs de noeuds en perspective, rappelez-vous l'entropie définit un état de désordre croissant...</p>                     
                

    
                   
