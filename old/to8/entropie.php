            <h4>Science & Vie Micro n°38 (avril 1987)</h4>

            <br>
            <p>Un coup d'oeil au Petit Robert tout d'abord, pour se remémorer la notion d'entropie: "En thermodynamique, se dit d'une fonction définissant l'état de désorde d'un système, croissante lorsque celle-ci évolue vers un autre état de désordre accru." Le message est clair, vous êtes chargé, sous les traits du robot Explorer 3, de rétablir l'équilibre des forces sur une planète en plein chaos. Monté sur coussin d'air, l'androïde arpente des cavernes en trois dimensions en collectant informations et objets sur son passage.</p>

            <p style="text-align:center;margin-top:25px;">
